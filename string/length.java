package string;

public class length {
	public static void main(String[] args) {
		String s="jds";
		int count=s.length();
		System.out.println(count);
		
		String rpl="SILAMBU";
		String fgh=rpl.toLowerCase();
		System.out.println(fgh);
		
		String s1="silambu";
		String s2=s1.toUpperCase();
		System.out.println(s2);
		
	}

}
